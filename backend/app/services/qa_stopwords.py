# 编译为 set 加速查找
STOP_SET = frozenset(_STOP_WORDS)


def normalize_query(text: str, keep_min_len: int = 1) -> str:
    """
    剥离停用词，返回核心语义短语
    
    "请问下午茶折扣信息是什么？" → "下午茶 折扣 信息"
    "品牌咖啡满减券每人限领多少张？" → "品牌咖啡 满减券 每人 限领 张"
    """
    words = jieba.lcut(text)
    filtered = [w.strip() for w in words if w.strip() and w.strip() not in STOP_SET and len(w.strip()) >= keep_min_len]
    return " ".join(filtered) if filtered else text.strip()


def get_content_tokens(text: str) -> list:
    """获取内容词（过滤停用词后的词列表）"""
    words = jieba.lcut(text)
    return [w.strip() for w in words if w.strip() and w.strip() not in STOP_SET and len(w.strip()) >= 1]
